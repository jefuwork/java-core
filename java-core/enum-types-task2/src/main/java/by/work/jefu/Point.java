package by.work.jefu;

/**
 * Represents point.
 */
public class Point {
    /**
     * X.
     */
    private int x;
    
    /**
     * Y.
     */
    private int y;
    
    /**
     * Public empty constructor.
     */
    public Point() {
        //empty
    }
    
    /**
     * Getter for x.
     * @return this.x int type.
     */
    public int getX() {
        return this.x;
    }
    
    /**
     * Setter for x.
     * @param x int type.
     */
    public void setX(int x) {
        this.x = x;
    }
    
    /**
     * Getter for y.
     * @return this.y int type.
     */
    public int getY() {
        return this.y;
    }
    
    /**
     * Setter for y.
     * @param y int type.
     */
    public void setY(int y) {
        this.y = y;
    }
}
