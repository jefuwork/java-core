package by.work.jefu;

/**
 * Interface for advanced media player.
 */
public interface AdvancedMediaPlayer {
    /**
     * Plays vlc format.
     * @param filename String type.
     */
    void playVlc(String filename);
    /**
     * Plays mp4 format.
     * @param filename String type.
     */
    void playMp4(String filename);
}
